import type { Lang } from "./i18n";

export type RiskLevel = "NONE" | "LOW" | "MODERATE" | "HIGH" | "IMMINENT";

export type Answers = {
  q1?: boolean; // желание не жить
  q2?: boolean; // суицидальные мысли
  q3?: boolean; // обдумывание способа
  q4?: boolean; // мысли с намерением
  q5?: boolean; // конкретный план + намерение
  q6?: boolean; // суицидальное поведение когда-либо
  q6recent?: boolean; // поведение за последние 3 месяца
};

// Классификация по логике Columbia Protocol (упрощённый триаж).
export function classifyRisk(a: Answers): RiskLevel {
  if (a.q5 || a.q6recent) return "IMMINENT";
  if (a.q4 || a.q6) return "HIGH";
  if (a.q2 || a.q3) return "MODERATE";
  if (a.q1) return "LOW";
  return "NONE";
}

// Нужна ли немедленная эскалация (takeover + кейс супервайзеру).
export function needsEscalation(level: RiskLevel): boolean {
  return level === "HIGH" || level === "IMMINENT";
}

